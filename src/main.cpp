#include <iostream>
#include <bitset>
#include "CPU.h"

int main(int argc, char** argv) {
	
	CPU cpu;
	int p_size = 16;
	std::bitset<16> p[] = {
		0b0000000000000000,
		0b1111110000010000,
		0b0000000000000001,
		0b1111010011010000,
		0b0000000000001010,
		0b1110001100000001,
		0b0000000000000001,
		0b1111110000010000,
		0b0000000000001100,
		0b1110101010000111,
		0b0000000000000000,
		0b1111110000010000,
		0b0000000000000010,
		0b1110001100001000,
		0b0000000000001110,
		0b1110101010000111
	};

	cpu.setMemory(0, 44);
	cpu.setMemory(1, 23);
	cpu.loadProgram(p, p_size);
	cpu.run();


	return 0;
}